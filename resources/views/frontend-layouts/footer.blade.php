<script>
    document.addEventListener("DOMContentLoaded", () => {
        const menuItems = document.querySelectorAll(".list .has-submenu");

        menuItems.forEach((item) => {
            item.addEventListener("click", (e) => {
                // Close other submenus if needed
                document.querySelectorAll(".list .submenu").forEach((submenu) => {
                    if (submenu !== item.querySelector(".submenu")) {
                        submenu.style.display = "none";
                    }
                });

                // Toggle submenu display
                const submenu = item.querySelector(".submenu");
                if (submenu) {
                    const isVisible = submenu.style.display === "block";
                    submenu.style.display = isVisible ? "none" : "block";

                    // Prevent overflow
                    const rect = submenu.getBoundingClientRect();
                    if (rect.right > window.innerWidth) {
                        submenu.style.left = "auto";
                        submenu.style.right = "0";
                    }
                }
            });
        });

        // Close submenu if clicking outside
        document.addEventListener("click", (e) => {
            if (!e.target.closest(".has-submenu")) {
                document.querySelectorAll(".list .submenu").forEach((submenu) => {
                    submenu.style.display = "none";
                });
            }
        });
    });
</script>
